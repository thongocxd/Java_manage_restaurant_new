    private void loadDataFromDatabase() {
        MongoClient mongoClient = null;
        MongoCursor<Document> cursor = null;
        try {
            mongoClient = MongoClients.create("mongodb+srv://phucpro2104:phuc123@cluster0.7834cva.mongodb.net/test"); // Thay thế với connection string thực tế
            MongoDatabase database = mongoClient.getDatabase("restaurant");
            MongoCollection<Document> collection = database.getCollection("users");

            Document query = new Document("_id", idNV);
            cursor = collection.find(query).iterator();

            if (cursor.hasNext()) {
                Document doc = cursor.next();
                jTextField2.setText(doc.getString("Ten")); // Tên nhân viên
                jTextField3.setText(doc.getString("username")); // Tài khoản
                jTextField4.setText(doc.getString("Matkhau")); // Mật khẩu
                jTextField5.setText(doc.getString("Email")); // Email
                // Các trường khác tương tự
            }
        } catch (Exception e) {
            e.printStackTrace();
            // Xử lý lỗi tại đây
        } finally {
            if (cursor != null) {
                cursor.close();
            }
            if (mongoClient != null) {
                mongoClient.close();
            }
        }
    }

